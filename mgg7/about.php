    <h1>KMPA ITERA</h1>
    <p>
        KMPA ITERA, merupakan organisasi internal kampus ITERA yang merupakan bagian dari Keluraga Mahasiswa ITERA (KM ITERA) dalam bentuk unit kegiatan mahasiswa (UKM) dengan kegiatan tematik utamannya adalah outdoor activity seperti hiking, camping, dan kegiatan alam bebas lainnya namun tidak menutup kemungkinan aktif dalam kegiatan internal kampus ataupun indoor activity.
        Pada dasarnya, KMPA diinisiasi untuk mewadahi perkumpulan mahasiswa ITERA yang gemar berkegiatan outdoor namun visi kedepannya diharapkan KMPA juga mampu berkontribusi nyata terhadap pelestarian alam. Awal mulanya KMPA diinisiasi oleh beberapa mahasiswa dari angkatan 2012 yang beberapa kesempatan melakukan kegiatan outdoor dan mendaki gunung Bersama yaitu: Gading Nur Rahmat, Alief Bani Ghazi, Bernardus Galih DW, Bramah Ing Sona, Ragil Karim, Fahmi Adi W, dan Christopher Immanuel.
        Maka untuk selanjutnya tercetuslah membentuk sebuah Lembaga agar lebih terkoordinasi kegiatannya dan lebih banyak teman-teman mahasiswa yang bergabung dan berkegiatan bersama. Pada tanggal 7 Maret 2013 KMPA terbentuk dengan 7 anggota lalu kembali melakukan aktivitas pendakian dengan merekrut massa kampus yang lebih banyak lagi hingga akhirnya pada tanggal 6 Oktober 2015, KMPA diresmikan oleh pihak Rektorat ITERA sebagai salah satu organisasi kemahasiswaan yang legal dalam KM ITERA bersamaan dengan organisasi kemahasiswaan lainnya.
